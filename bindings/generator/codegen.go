// knownEnumNames maps enum type name → whether it is a unit-only enum
// (true) or a discriminated-union enum (false). This lets codegen choose the
// correct Go zero value: unit-only enums are `uint32` newtypes (zero `0`),
// union enums are structs (zero `T{}`).
var knownEnumNames = map[string]bool{}

func isEnumType(rustType string) bool {
	name := extractStructName(rustType)
	_, ok := knownEnumNames[name]
	return ok
}

// isUnitEnumType reports whether the named enum is unit-only. Returns false
// for non-enum types and for discriminated-union enums.
func isUnitEnumType(rustType string) bool {
	name := extractStructName(rustType)
	isUnit, ok := knownEnumNames[name]
	return ok && isUnit
}

// typesFileNeedsImports reports which standard imports types.go needs for the
// parsed contract. Event-only modules (e.g. rmn_remote after config removal)
// emit plain Go structs and need no fmt/scval/xdr imports.
func typesFileNeedsImports(contract *Contract) (needFmt, needScval, needXdr bool) {
	if len(contract.Structs) > 0 || len(contract.Errors) > 0 || len(contract.Enums) > 0 {
		return true, true, true
	}
	for _, e := range contract.Events {
		for _, f := range e.Fields {
			goType := rustTypeToGo(f.Type)
			if strings.Contains(goType, "scval.") {
				needScval = true
			}
		}
	}
	return needFmt, needScval, needXdr
}

// GenerateTypes generates the types.go file content.
func GenerateTypes(pkg string, contract *Contract) string {
	knownEnumNames = map[string]bool{}
	for _, e := range contract.Enums {
		knownEnumNames[e.Name] = e.IsUnit()
	}

	var b strings.Builder

	b.WriteString("// Code generated by stellar-bindings-generator. DO NOT EDIT.\n")
	b.WriteString(fmt.Sprintf("package %s\n\n", pkg))

	needFmt, needScval, needXdr := typesFileNeedsImports(contract)
	if needFmt || needScval || needXdr {
		b.WriteString("import (\n")
		if needFmt {
			b.WriteString("\t\"fmt\"\n")
		}
		if needScval || needXdr {
			if needFmt {
				b.WriteString("\n")
			}
			if needScval {
				b.WriteString("\t\"github.com/smartcontractkit/chainlink-stellar/bindings/scval\"\n")
			}
			if needXdr {
				b.WriteString("\t\"github.com/stellar/go-stellar-sdk/xdr\"\n")
			}
		}
		b.WriteString(")\n\n")
	}

	// Generate structs
	for _, s := range contract.Structs {
		generateStruct(&b, s)
	}

	// Generate error enums
	for _, e := range contract.Errors {
		generateErrorEnum(&b, e)
	}

	// Generate events
	for _, e := range contract.Events {
		generateEventStruct(&b, e)
	}

	// Generate enums
	for _, e := range contract.Enums {
		generateEnum(&b, e)
	}

	return b.String()
}

func generateStruct(b *strings.Builder, s Struct) {
	// Go struct
	b.WriteString(fmt.Sprintf("// %s represents the %s struct from the contract.\n", s.Name, s.Name))
	b.WriteString(fmt.Sprintf("type %s struct {\n", s.Name))
	for _, f := range s.Fields {
		goType := rustTypeToGo(f.Type)
		b.WriteString(fmt.Sprintf("\t%s %s\n", snakeToPascal(f.Name), goType))
	}
	b.WriteString("}\n\n")

	// ToScVal method - use value receiver for generics compatibility
	b.WriteString(fmt.Sprintf("// ToScVal converts %s to an xdr.ScVal for contract calls.\n", s.Name))
	b.WriteString(fmt.Sprintf("func (s %s) ToScVal() (xdr.ScVal, error) {\n", s.Name))
	b.WriteString("\treturn scval.BuildStructScVal(map[string]xdr.ScVal{\n")
	for _, f := range s.Fields {
		converter := getToScValConverter(f.Type, "s."+snakeToPascal(f.Name))
		b.WriteString(fmt.Sprintf("\t\t\"%s\": %s,\n", f.Name, converter))
	}
	b.WriteString("\t})\n")
	b.WriteString("}\n\n")

	// FromScVal function
	b.WriteString(fmt.Sprintf("// %sFromScVal parses an xdr.ScVal into %s.\n", s.Name, s.Name))
	b.WriteString(fmt.Sprintf("func %sFromScVal(val xdr.ScVal) (*%s, error) {\n", s.Name, s.Name))
	b.WriteString("\tscMap, ok := val.GetMap()\n")
	b.WriteString("\tif !ok || scMap == nil {\n")
	b.WriteString("\t\treturn nil, fmt.Errorf(\"not a map type\")\n")
	b.WriteString("\t}\n\n")
	b.WriteString(fmt.Sprintf("\tresult := &%s{}\n", s.Name))
	b.WriteString("\tfor _, entry := range *scMap {\n")
	b.WriteString("\t\tkey, ok := entry.Key.GetSym()\n")
	b.WriteString("\t\tif !ok {\n")
	b.WriteString("\t\t\tcontinue\n")
	b.WriteString("\t\t}\n\n")
	b.WriteString("\t\tswitch string(key) {\n")
	for _, f := range s.Fields {
		b.WriteString(fmt.Sprintf("\t\tcase \"%s\":\n", f.Name))
		generateFromScValField(b, f, "result."+snakeToPascal(f.Name))
	}
	b.WriteString("\t\t}\n")
	b.WriteString("\t}\n\n")
	b.WriteString("\treturn result, nil\n")
	b.WriteString("}\n\n")
}

func generateFromScValField(b *strings.Builder, f Field, target string) {
	switch {
	case f.Type == "u64":
		b.WriteString(fmt.Sprintf("\t\t\tv, err := scval.Uint64FromScVal(entry.Val)\n"))
		b.WriteString("\t\t\tif err != nil {\n")
		b.WriteString(fmt.Sprintf("\t\t\t\treturn nil, fmt.Errorf(\"%s: %%w\", err)\n", f.Name))
		b.WriteString("\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t%s = v\n", target))
	case f.Type == "u32":
		b.WriteString("\t\t\tv, ok := entry.Val.GetU32()\n")
		b.WriteString("\t\t\tif !ok {\n")
		b.WriteString(fmt.Sprintf("\t\t\t\treturn nil, fmt.Errorf(\"%s is not u32\")\n", f.Name))
		b.WriteString("\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t%s = uint32(v)\n", target))
	case f.Type == "i128":
		b.WriteString("\t\t\tv, err := scval.I128FromScVal(entry.Val)\n")
		b.WriteString("\t\t\tif err != nil {\n")
		b.WriteString(fmt.Sprintf("\t\t\t\treturn nil, fmt.Errorf(\"%s: %%w\", err)\n", f.Name))
		b.WriteString("\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t%s = v\n", target))
	case f.Type == "bool":
		b.WriteString("\t\t\tv, ok := entry.Val.GetB()\n")
		b.WriteString("\t\t\tif !ok {\n")
		b.WriteString(fmt.Sprintf("\t\t\t\treturn nil, fmt.Errorf(\"%s is not bool\")\n", f.Name))
		b.WriteString("\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t%s = v\n", target))
	case f.Type == "soroban_sdk::Address":
		b.WriteString("\t\t\tv, err := scval.AddressFromScVal(entry.Val)\n")
		b.WriteString("\t\t\tif err != nil {\n")
		b.WriteString(fmt.Sprintf("\t\t\t\treturn nil, fmt.Errorf(\"%s: %%w\", err)\n", f.Name))
		b.WriteString("\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t%s = v\n", target))
	case f.Type == "soroban_sdk::Bytes":
		b.WriteString("\t\t\tv, ok := entry.Val.GetBytes()\n")
		b.WriteString("\t\t\tif !ok {\n")
		b.WriteString(fmt.Sprintf("\t\t\t\treturn nil, fmt.Errorf(\"%s is not bytes\")\n", f.Name))
		b.WriteString("\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t%s = []byte(v)\n", target))
	case f.Type == "soroban_sdk::String":
		b.WriteString("\t\t\tv, err := scval.StringFromScVal(entry.Val)\n")
		b.WriteString("\t\t\tif err != nil {\n")
		b.WriteString(fmt.Sprintf("\t\t\t\treturn nil, fmt.Errorf(\"%s: %%w\", err)\n", f.Name))
		b.WriteString("\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t%s = v\n", target))
	case f.Type == "soroban_sdk::Symbol":
		b.WriteString("\t\t\tv, err := scval.SymbolFromScVal(entry.Val)\n")
		b.WriteString("\t\t\tif err != nil {\n")
		b.WriteString(fmt.Sprintf("\t\t\t\treturn nil, fmt.Errorf(\"%s: %%w\", err)\n", f.Name))
		b.WriteString("\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t%s = v\n", target))
	case f.Type == "u128":
		b.WriteString("\t\t\tv, err := scval.U128FromScVal(entry.Val)\n")
		b.WriteString("\t\t\tif err != nil {\n")
		b.WriteString(fmt.Sprintf("\t\t\t\treturn nil, fmt.Errorf(\"%s: %%w\", err)\n", f.Name))
		b.WriteString("\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t%s = v\n", target))
	case strings.HasPrefix(f.Type, "Option<") && strings.Contains(f.Type, "soroban_sdk::Address"):
		b.WriteString("\t\t\tv, err := scval.OptionalAddressFromScVal(entry.Val)\n")
		b.WriteString("\t\t\tif err != nil {\n")
		b.WriteString(fmt.Sprintf("\t\t\t\treturn nil, fmt.Errorf(\"%s: %%w\", err)\n", f.Name))
		b.WriteString("\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t%s = v\n", target))
	case strings.HasPrefix(f.Type, "soroban_sdk::BytesN<"):
		n := extractBytesNSize(f.Type)
		b.WriteString(fmt.Sprintf("\t\t\tv, err := scval.Bytes%dFromScVal(entry.Val)\n", n))
		b.WriteString("\t\t\tif err != nil {\n")
		b.WriteString(fmt.Sprintf("\t\t\t\treturn nil, fmt.Errorf(\"%s: %%w\", err)\n", f.Name))
		b.WriteString("\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t%s = v\n", target))
	case strings.HasPrefix(f.Type, "soroban_sdk::Vec<"):
		innerType := extractVecInnerType(f.Type)
		b.WriteString("\t\t\tvec, ok := entry.Val.GetVec()\n")
		b.WriteString("\t\t\tif !ok || vec == nil {\n")
		b.WriteString(fmt.Sprintf("\t\t\t\treturn nil, fmt.Errorf(\"%s is not a vec\")\n", f.Name))
		b.WriteString("\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t%s = make(%s, len(*vec))\n", target, rustTypeToGo(f.Type)))
		b.WriteString("\t\t\tfor i, item := range *vec {\n")
		generateVecItemParse(b, innerType, target+"[i]")
		b.WriteString("\t\t\t}\n")
	case isEnumType(f.Type):
		enumName := extractStructName(f.Type)
		b.WriteString(fmt.Sprintf("\t\t\tv, err := %sFromScVal(entry.Val)\n", enumName))
		b.WriteString("\t\t\tif err != nil {\n")
		b.WriteString(fmt.Sprintf("\t\t\t\treturn nil, fmt.Errorf(\"%s: %%w\", err)\n", f.Name))
		b.WriteString("\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t%s = v\n", target))
	default:
		// Nested struct
		structName := extractStructName(f.Type)
		b.WriteString(fmt.Sprintf("\t\t\tv, err := %sFromScVal(entry.Val)\n", structName))
		b.WriteString("\t\t\tif err != nil {\n")
		b.WriteString(fmt.Sprintf("\t\t\t\treturn nil, fmt.Errorf(\"%s: %%w\", err)\n", f.Name))
		b.WriteString("\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t%s = *v\n", target))
	}
}

func generateVecItemParse(b *strings.Builder, innerType, target string) {
	switch innerType {
	case "u64":
		b.WriteString("\t\t\t\tv, err := scval.Uint64FromScVal(item)\n")
		b.WriteString("\t\t\t\tif err != nil {\n")
		b.WriteString("\t\t\t\t\treturn nil, err\n")
		b.WriteString("\t\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t\t%s = v\n", target))
	case "u32":
		b.WriteString("\t\t\t\tv, err := scval.Uint32FromScVal(item)\n")
		b.WriteString("\t\t\t\tif err != nil {\n")
		b.WriteString("\t\t\t\t\treturn nil, err\n")
		b.WriteString("\t\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t\t%s = v\n", target))
	case "soroban_sdk::Address":
		b.WriteString("\t\t\t\tv, err := scval.AddressFromScVal(item)\n")
		b.WriteString("\t\t\t\tif err != nil {\n")
		b.WriteString("\t\t\t\t\treturn nil, err\n")
		b.WriteString("\t\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t\t%s = v\n", target))
	case "soroban_sdk::Bytes":
		b.WriteString("\t\t\t\tv, ok := item.GetBytes()\n")
		b.WriteString("\t\t\t\tif !ok {\n")
		b.WriteString("\t\t\t\t\treturn nil, fmt.Errorf(\"vec item is not bytes\")\n")
		b.WriteString("\t\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t\t%s = []byte(v)\n", target))
	case "soroban_sdk::String":
		b.WriteString("\t\t\t\tv, err := scval.StringFromScVal(item)\n")
		b.WriteString("\t\t\t\tif err != nil {\n")
		b.WriteString("\t\t\t\t\treturn nil, err\n")
		b.WriteString("\t\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t\t%s = v\n", target))
	case "u128":
		b.WriteString("\t\t\t\tv, err := scval.U128FromScVal(item)\n")
		b.WriteString("\t\t\t\tif err != nil {\n")
		b.WriteString("\t\t\t\t\treturn nil, err\n")
		b.WriteString("\t\t\t\t}\n")
		b.WriteString(fmt.Sprintf("\t\t\t\t%s = v\n", target))
	default:
		if strings.HasPrefix(innerType, "Option<") && strings.Contains(innerType, "soroban_sdk::Address") {
			b.WriteString("\t\t\t\tv, err := scval.OptionalAddressFromScVal(item)\n")
			b.WriteString("\t\t\t\tif err != nil {\n")
			b.WriteString("\t\t\t\t\treturn nil, err\n")
			b.WriteString("\t\t\t\t}\n")
			b.WriteString(fmt.Sprintf("\t\t\t\t%s = v\n", target))
		} else if n := extractBytesNSize(innerType); n > 0 {
			b.WriteString(fmt.Sprintf("\t\t\t\tv, err := scval.Bytes%dFromScVal(item)\n", n))
			b.WriteString("\t\t\t\tif err != nil {\n")
			b.WriteString("\t\t\t\t\treturn nil, err\n")
			b.WriteString("\t\t\t\t}\n")
			b.WriteString(fmt.Sprintf("\t\t\t\t%s = v\n", target))
		} else if isEnumType(innerType) {
			enumName := extractStructName(innerType)
			b.WriteString(fmt.Sprintf("\t\t\t\tv, err := %sFromScVal(item)\n", enumName))
			b.WriteString("\t\t\t\tif err != nil {\n")
			b.WriteString("\t\t\t\t\treturn nil, err\n")
			b.WriteString("\t\t\t\t}\n")
			b.WriteString(fmt.Sprintf("\t\t\t\t%s = v\n", target))
		} else {
			// Nested struct in vec
			structName := extractStructName(innerType)
			b.WriteString(fmt.Sprintf("\t\t\t\tv, err := %sFromScVal(item)\n", structName))
			b.WriteString("\t\t\t\tif err != nil {\n")
			b.WriteString("\t\t\t\t\treturn nil, err\n")
			b.WriteString("\t\t\t\t}\n")
			b.WriteString(fmt.Sprintf("\t\t\t\t%s = *v\n", target))
		}
	}
}

func generateErrorEnum(b *strings.Builder, e ErrorEnum) {
	b.WriteString(fmt.Sprintf("// %s represents the contract error codes.\n", e.Name))
	b.WriteString("const (\n")
	for _, v := range e.Variants {
		b.WriteString(fmt.Sprintf("\t%s%s = %d\n", e.Name, v.Name, v.Value))
	}
	b.WriteString(")\n\n")

	// Error message map
	b.WriteString(fmt.Sprintf("// %sMessage returns a human-readable message for error codes.\n", e.Name))
	b.WriteString(fmt.Sprintf("var %sMessage = map[int]string{\n", e.Name))
	for _, v := range e.Variants {
		b.WriteString(fmt.Sprintf("\t%d: \"%s\",\n", v.Value, camelToSpaces(v.Name)))
	}
	b.WriteString("}\n\n")
}

// generateEnum dispatches between the two valid Soroban #[contracttype] enum
// encodings: unit-only enums become Go uint32 newtypes (ScVal::U32 wire
// format), and any enum with a tuple/struct variant becomes a discriminated
// union encoded as ScVal::Vec([Symbol(<VariantName>), <payload-fields...>]).
//
// IMPORTANT: the variant identifier is used **verbatim** as the discriminant
// symbol — Soroban does not snake_case it. This must match the on-chain
// encoding produced by `#[contracttype]` derive on the Rust side.
func generateEnum(b *strings.Builder, e Enum) {
	if e.IsUnit() {
		generateUnitEnum(b, e)
		return
	}
	generateUnionEnum(b, e)
}

func generateUnitEnum(b *strings.Builder, e Enum) {
	b.WriteString(fmt.Sprintf("// %s represents the %s enum (unit-only Soroban contracttype, encoded as ScVal::U32).\n", e.Name, e.Name))
	b.WriteString(fmt.Sprintf("type %s uint32\n\n", e.Name))
	b.WriteString("const (\n")
	for _, v := range e.Variants {
		b.WriteString(fmt.Sprintf("\t%s%s %s = %d\n", e.Name, v.Name, e.Name, v.Value))
	}
	b.WriteString(")\n\n")

	b.WriteString(fmt.Sprintf("// ToScVal converts %s to an xdr.ScVal.\n", e.Name))
	b.WriteString(fmt.Sprintf("func (e %s) ToScVal() (xdr.ScVal, error) {\n", e.Name))
	b.WriteString("\treturn scval.Uint32ToScVal(uint32(e)), nil\n")
	b.WriteString("}\n\n")

	b.WriteString(fmt.Sprintf("// %sFromScVal parses an xdr.ScVal into %s.\n", e.Name, e.Name))
	b.WriteString(fmt.Sprintf("func %sFromScVal(val xdr.ScVal) (%s, error) {\n", e.Name, e.Name))
	b.WriteString("\tv, ok := val.GetU32()\n")
	b.WriteString("\tif !ok {\n")
	b.WriteString(fmt.Sprintf("\t\treturn 0, fmt.Errorf(\"expected u32 for %s enum\")\n", e.Name))
	b.WriteString("\t}\n")
	b.WriteString(fmt.Sprintf("\treturn %s(v), nil\n", e.Name))
	b.WriteString("}\n\n")
}

// generateUnionEnum emits a discriminated-union Go shape for any enum that
// has at least one tuple or struct variant.
//
// The Go layout is one outer struct with one optional pointer per variant.
// Exactly one pointer must be non-nil for a value to be valid; multiple
// non-nil pointers cause `ToScVal` to fail loudly. Unit variants are
// represented by an empty marker struct so they can be set the same way
// (`X{Foo: &PoolDataKeyFoo{}}`) as payload-bearing ones.
func generateUnionEnum(b *strings.Builder, e Enum) {
	b.WriteString(fmt.Sprintf("// %s is a Soroban discriminated-union (#[contracttype] enum with payload(s)).\n", e.Name))
	b.WriteString("// Wire format: ScVal::Vec([ScVal::Symbol(<VariantName>), <payload fields...>]).\n")
	b.WriteString("// Construct by setting exactly one variant pointer to a non-nil value.\n")
	b.WriteString(fmt.Sprintf("type %s struct {\n", e.Name))
	for _, v := range e.Variants {
		b.WriteString(fmt.Sprintf("\t%s *%s%s\n", v.Name, e.Name, v.Name))
	}
	b.WriteString("}\n\n")

	for _, v := range e.Variants {
		generateUnionVariantStruct(b, e, v)
	}

	generateUnionToScVal(b, e)
	generateUnionFromScVal(b, e)
}

func generateUnionVariantStruct(b *strings.Builder, e Enum, v EnumVariant) {
	typeName := e.Name + v.Name
	switch v.Kind {
	case EnumVariantUnit:
		b.WriteString(fmt.Sprintf("// %s is the unit variant %s::%s.\n", typeName, e.Name, v.Name))
		b.WriteString(fmt.Sprintf("type %s struct{}\n\n", typeName))
	case EnumVariantTuple:
		b.WriteString(fmt.Sprintf("// %s is the tuple variant %s::%s.\n", typeName, e.Name, v.Name))
		b.WriteString(fmt.Sprintf("type %s struct {\n", typeName))
		for i, f := range v.Payload {
			b.WriteString(fmt.Sprintf("\t%s %s\n", tupleFieldName(i), rustTypeToGo(f.Type)))
		}
		b.WriteString("}\n\n")
	case EnumVariantStruct:
		b.WriteString(fmt.Sprintf("// %s is the struct variant %s::%s.\n", typeName, e.Name, v.Name))
		b.WriteString(fmt.Sprintf("type %s struct {\n", typeName))
		for _, f := range v.Payload {
			b.WriteString(fmt.Sprintf("\t%s %s\n", snakeToPascal(f.Name), rustTypeToGo(f.Type)))
		}
		b.WriteString("}\n\n")
	}
}

// tupleFieldName produces the Go field name we use for the i-th positional
// payload of a tuple variant. We exposed it as `Field0`, `Field1`, ... rather
// than `_0`/`_1` so the field is exported (Go restricts non-ASCII or leading
// underscore exports) and accessible to callers building keys for
// RestoreFootprintOp.
func tupleFieldName(i int) string {
	return fmt.Sprintf("Field%d", i)
}

func generateUnionToScVal(b *strings.Builder, e Enum) {
	b.WriteString(fmt.Sprintf("// ToScVal converts %s to its Soroban discriminated-union encoding.\n", e.Name))
	b.WriteString(fmt.Sprintf("// Returns an error if zero or multiple variant pointers are set.\n"))
	b.WriteString(fmt.Sprintf("func (e %s) ToScVal() (xdr.ScVal, error) {\n", e.Name))
	b.WriteString("\tset := 0\n")
	for _, v := range e.Variants {
		b.WriteString(fmt.Sprintf("\tif e.%s != nil { set++ }\n", v.Name))
	}
	b.WriteString("\tif set != 1 {\n")
	b.WriteString(fmt.Sprintf("\t\treturn xdr.ScVal{}, fmt.Errorf(\"%s: expected exactly one variant set, got %%d\", set)\n", e.Name))
	b.WriteString("\t}\n")
	for _, v := range e.Variants {
		b.WriteString(fmt.Sprintf("\tif e.%s != nil {\n", v.Name))
		// Build the ScVec: [Symbol(VariantName), payload...]
		b.WriteString("\t\titems := []xdr.ScVal{\n")
		b.WriteString(fmt.Sprintf("\t\t\tscval.SymbolToScVal(%q),\n", v.Name))
		switch v.Kind {
		case EnumVariantUnit:
			// no payload fields
		case EnumVariantTuple:
			for i, f := range v.Payload {
				expr := fmt.Sprintf("e.%s.%s", v.Name, tupleFieldName(i))
				b.WriteString(fmt.Sprintf("\t\t\t%s,\n", payloadToScValExpr(f.Type, expr)))
			}
		case EnumVariantStruct:
			for _, f := range v.Payload {
				expr := fmt.Sprintf("e.%s.%s", v.Name, snakeToPascal(f.Name))
				b.WriteString(fmt.Sprintf("\t\t\t%s,\n", payloadToScValExpr(f.Type, expr)))
			}
		}
		b.WriteString("\t\t}\n")
		b.WriteString("\t\treturn scval.VecToScVal(items), nil\n")
		b.WriteString("\t}\n")
	}
	// Unreachable due to the `set != 1` guard above, but the Go compiler
	// can't see that.
	b.WriteString(fmt.Sprintf("\treturn xdr.ScVal{}, fmt.Errorf(\"%s: unreachable\")\n", e.Name))
	b.WriteString("}\n\n")
}

func generateUnionFromScVal(b *strings.Builder, e Enum) {
	b.WriteString(fmt.Sprintf("// %sFromScVal parses an xdr.ScVal into %s.\n", e.Name, e.Name))
	b.WriteString(fmt.Sprintf("func %sFromScVal(val xdr.ScVal) (%s, error) {\n", e.Name, e.Name))
	b.WriteString(fmt.Sprintf("\tvecPtr, ok := val.GetVec()\n"))
	b.WriteString(fmt.Sprintf("\tif !ok || vecPtr == nil || *vecPtr == nil {\n"))
	b.WriteString(fmt.Sprintf("\t\treturn %s{}, fmt.Errorf(\"expected vec for %s enum\")\n", e.Name, e.Name))
	b.WriteString("\t}\n")
	b.WriteString("\tvec := *vecPtr\n")
	b.WriteString("\tif len(vec) < 1 {\n")
	b.WriteString(fmt.Sprintf("\t\treturn %s{}, fmt.Errorf(\"%s: empty vec\")\n", e.Name, e.Name))
	b.WriteString("\t}\n")
	b.WriteString("\ttag, err := scval.SymbolFromScVal(vec[0])\n")
	b.WriteString("\tif err != nil {\n")
	b.WriteString(fmt.Sprintf("\t\treturn %s{}, fmt.Errorf(\"%s: variant tag: %%w\", err)\n", e.Name, e.Name))
	b.WriteString("\t}\n")
	b.WriteString("\tswitch tag {\n")
	for _, v := range e.Variants {
		b.WriteString(fmt.Sprintf("\tcase %q:\n", v.Name))
		expectedLen := 1 + len(v.Payload)
		b.WriteString(fmt.Sprintf("\t\tif len(vec) != %d {\n", expectedLen))
		b.WriteString(fmt.Sprintf("\t\t\treturn %s{}, fmt.Errorf(\"%s::%s: expected %d elements, got %%d\", len(vec))\n", e.Name, e.Name, v.Name, expectedLen))
		b.WriteString("\t\t}\n")
		typeName := e.Name + v.Name
		switch v.Kind {
		case EnumVariantUnit:
			b.WriteString(fmt.Sprintf("\t\treturn %s{%s: &%s{}}, nil\n", e.Name, v.Name, typeName))
		case EnumVariantTuple:
			b.WriteString(fmt.Sprintf("\t\tpayload := &%s{}\n", typeName))
			for i, f := range v.Payload {
				assign := fmt.Sprintf("payload.%s", tupleFieldName(i))
				generatePayloadFromScVal(b, f.Type, fmt.Sprintf("vec[%d]", i+1), assign, fmt.Sprintf("%s::%s[%d]", e.Name, v.Name, i), e.Name)
			}
			b.WriteString(fmt.Sprintf("\t\treturn %s{%s: payload}, nil\n", e.Name, v.Name))
		case EnumVariantStruct:
			b.WriteString(fmt.Sprintf("\t\tpayload := &%s{}\n", typeName))
			for i, f := range v.Payload {
				assign := fmt.Sprintf("payload.%s", snakeToPascal(f.Name))
				generatePayloadFromScVal(b, f.Type, fmt.Sprintf("vec[%d]", i+1), assign, fmt.Sprintf("%s::%s.%s", e.Name, v.Name, f.Name), e.Name)
			}
			b.WriteString(fmt.Sprintf("\t\treturn %s{%s: payload}, nil\n", e.Name, v.Name))
		}
	}
	b.WriteString("\tdefault:\n")
	b.WriteString(fmt.Sprintf("\t\treturn %s{}, fmt.Errorf(\"%s: unknown variant %%q\", tag)\n", e.Name, e.Name))
	b.WriteString("\t}\n")
	b.WriteString("}\n\n")
}

// payloadToScValExpr returns the Go expression that converts a single payload
// field (of the given Rust type) to an xdr.ScVal. We delegate to the existing
// converter used for struct fields, which already handles all the supported
// Rust→Soroban scalar/composite types.
func payloadToScValExpr(rustType, expr string) string {
	return getToScValConverter(rustType, expr)
}

// generatePayloadFromScVal emits Go code that decodes vec[i] into the
// destination `assign` for an enum payload field. It mirrors the supported
// surface of getToScValConverter so encode/decode stays symmetric.
//
// errCtx is a human-readable context string used in error wrapping.
// enumName is used as the zero-value type for early returns.
func generatePayloadFromScVal(b *strings.Builder, rustType, src, assign, errCtx, enumName string) {
	zero := enumName + "{}"
	switch rustType {
	case "u32":
		b.WriteString(fmt.Sprintf("\t\tif v, ok := %s.GetU32(); ok { %s = uint32(v) } else { return %s, fmt.Errorf(\"%s: expected u32\") }\n", src, assign, zero, errCtx))
	case "u64":
		b.WriteString(fmt.Sprintf("\t\tif v, err := scval.Uint64FromScVal(%s); err != nil { return %s, fmt.Errorf(\"%s: %%w\", err) } else { %s = v }\n", src, zero, errCtx, assign))
	case "i128":
		b.WriteString(fmt.Sprintf("\t\tif v, err := scval.I128FromScVal(%s); err != nil { return %s, fmt.Errorf(\"%s: %%w\", err) } else { %s = v }\n", src, zero, errCtx, assign))
	case "u128":
		b.WriteString(fmt.Sprintf("\t\tif v, err := scval.U128FromScVal(%s); err != nil { return %s, fmt.Errorf(\"%s: %%w\", err) } else { %s = v }\n", src, zero, errCtx, assign))
	case "bool":
		b.WriteString(fmt.Sprintf("\t\tif v, ok := %s.GetB(); ok { %s = v } else { return %s, fmt.Errorf(\"%s: expected bool\") }\n", src, assign, zero, errCtx))
	case "soroban_sdk::Address":
		b.WriteString(fmt.Sprintf("\t\tif v, err := scval.AddressFromScVal(%s); err != nil { return %s, fmt.Errorf(\"%s: %%w\", err) } else { %s = v }\n", src, zero, errCtx, assign))
	case "soroban_sdk::Bytes":
		b.WriteString(fmt.Sprintf("\t\tif v, ok := %s.GetBytes(); ok { %s = []byte(v) } else { return %s, fmt.Errorf(\"%s: expected bytes\") }\n", src, assign, zero, errCtx))
	case "soroban_sdk::String":
		b.WriteString(fmt.Sprintf("\t\tif v, err := scval.StringFromScVal(%s); err != nil { return %s, fmt.Errorf(\"%s: %%w\", err) } else { %s = v }\n", src, zero, errCtx, assign))
	case "soroban_sdk::Symbol":
		b.WriteString(fmt.Sprintf("\t\tif v, err := scval.SymbolFromScVal(%s); err != nil { return %s, fmt.Errorf(\"%s: %%w\", err) } else { %s = v }\n", src, zero, errCtx, assign))
	default:
		switch {
		case strings.HasPrefix(rustType, "soroban_sdk::BytesN<"):
			n := extractBytesNSize(rustType)
			b.WriteString(fmt.Sprintf("\t\tif v, err := scval.Bytes%dFromScVal(%s); err != nil { return %s, fmt.Errorf(\"%s: %%w\", err) } else { %s = v }\n", n, src, zero, errCtx, assign))
		case isEnumType(rustType):
			enumInner := extractStructName(rustType)
			b.WriteString(fmt.Sprintf("\t\tif v, err := %sFromScVal(%s); err != nil { return %s, fmt.Errorf(\"%s: %%w\", err) } else { %s = v }\n", enumInner, src, zero, errCtx, assign))
		default:
			// Assume a struct; FromScVal returns *Struct.
			structName := extractStructName(rustType)
			b.WriteString(fmt.Sprintf("\t\tif v, err := %sFromScVal(%s); err != nil { return %s, fmt.Errorf(\"%s: %%w\", err) } else { %s = *v }\n", structName, src, zero, errCtx, assign))
		}
	}
}

func generateEventStruct(b *strings.Builder, e Event) {
	b.WriteString(fmt.Sprintf("// %s represents the %s event.\n", e.Name, e.Name))
	b.WriteString(fmt.Sprintf("// Topics: %v\n", e.Topics))
	b.WriteString(fmt.Sprintf("type %s struct {\n", e.Name))
	for _, f := range e.Fields {
		goType := rustTypeToGo(f.Type)
		b.WriteString(fmt.Sprintf("\t%s %s\n", snakeToPascal(f.Name), goType))
	}
	// Add metadata fields
	b.WriteString("\t// Event metadata\n")
	b.WriteString("\tLedger uint32\n")
	b.WriteString("\tTxHash string\n")
	b.WriteString("}\n\n")

	// Topic constant
	if len(e.Topics) > 0 {
		b.WriteString(fmt.Sprintf("// %sTopic is the event topic identifier.\n", e.Name))
		b.WriteString(fmt.Sprintf("const %sTopic = \"%s\"\n\n", e.Name, e.Topics[0]))
	}
}

// Type conversion helpers

func rustTypeToGo(rustType string) string {
	switch rustType {
	case "u64":
		return "uint64"
	case "u32":
		return "uint32"
	case "u128":
		return "scval.U128"
	case "i128":
		return "int64"
	case "bool":
		return "bool"
	case "soroban_sdk::Address":
		return "string"
	case "soroban_sdk::Bytes":
		return "[]byte"
	case "soroban_sdk::Symbol":
		return "string"
	case "soroban_sdk::String":
		return "string"
	}

	if strings.HasPrefix(rustType, "Option<") {
		inner := strings.TrimSuffix(strings.TrimPrefix(rustType, "Option<"), ">")
		inner = strings.TrimSpace(inner)
		if inner == "soroban_sdk::Address" {
			return "*string"
		}
		// Option<Struct> -> *Struct
		innerType := rustTypeToGo(inner)
		return "*" + innerType
	}

	if strings.HasPrefix(rustType, "soroban_sdk::BytesN<") {
		// Extract N
		n := strings.TrimSuffix(strings.TrimPrefix(rustType, "soroban_sdk::BytesN<"), ">")
		return fmt.Sprintf("[%s]byte", n)
	}

	if strings.HasPrefix(rustType, "soroban_sdk::Vec<") {
		inner := extractVecInnerType(rustType)
		return "[]" + rustTypeToGo(inner)
	}

	// Assume it's a struct name
	return extractStructName(rustType)
}

func extractVecInnerType(vecType string) string {
	// soroban_sdk::Vec<InnerType>
	inner := strings.TrimSuffix(strings.TrimPrefix(vecType, "soroban_sdk::Vec<"), ">")
	return strings.TrimSpace(inner)
}

// extractBytesNSize returns N from soroban_sdk::BytesN<N>, or 0 if not matched.
func extractBytesNSize(rustType string) int {
	if !strings.HasPrefix(rustType, "soroban_sdk::BytesN<") {
		return 0
	}
	nStr := strings.TrimSuffix(strings.TrimPrefix(rustType, "soroban_sdk::BytesN<"), ">")
	var n int
	fmt.Sscanf(strings.TrimSpace(nStr), "%d", &n)
	return n
}

func extractStructName(rustType string) string {
	// Handle things like TokenAmount or soroban_sdk types
	if strings.Contains(rustType, "::") {
		parts := strings.Split(rustType, "::")
		return parts[len(parts)-1]
	}
	return rustType
}

func getToScValConverter(rustType, expr string) string {
	switch rustType {
	case "u64":
		return fmt.Sprintf("scval.Uint64ToScVal(%s)", expr)
	case "u32":
		return fmt.Sprintf("scval.Uint32ToScVal(%s)", expr)
	case "u128":
		return fmt.Sprintf("scval.MustToScVal((%s).ToScVal())", expr)
	case "i128":
		return fmt.Sprintf("scval.I128ToScVal(%s)", expr)
	case "bool":
		return fmt.Sprintf("scval.BoolToScVal(%s)", expr)
	case "soroban_sdk::Address":
		return fmt.Sprintf("scval.AddressToScVal(%s)", expr)
	case "soroban_sdk::Bytes":
		return fmt.Sprintf("scval.BytesToScVal(%s)", expr)
	case "soroban_sdk::String":
		return fmt.Sprintf("scval.StringToScVal(%s)", expr)
	case "soroban_sdk::Symbol":
		return fmt.Sprintf("scval.SymbolToScVal(%s)", expr)
	}

	if strings.HasPrefix(rustType, "Option<") && strings.Contains(rustType, "soroban_sdk::Address") {
		return fmt.Sprintf("scval.OptionalAddressToScVal(%s)", expr)
	}

	if strings.HasPrefix(rustType, "soroban_sdk::BytesN<") {
		n := extractBytesNSize(rustType)
		return fmt.Sprintf("scval.Bytes%dToScVal(%s)", n, expr)
	}

	if strings.HasPrefix(rustType, "soroban_sdk::Vec<") {
		inner := extractVecInnerType(rustType)
		if inner == "u64" {
			return fmt.Sprintf("scval.Uint64SliceToScVal(%s)", expr)
		}
		if inner == "u32" {
			return fmt.Sprintf("scval.Uint32SliceToScVal(%s)", expr)
		}
		if inner == "soroban_sdk::Address" {
			return fmt.Sprintf("scval.AddressSliceToScVal(%s)", expr)
		}
		if inner == "soroban_sdk::Bytes" {
			return fmt.Sprintf("scval.BytesSliceToScVal(%s)", expr)
		}
		if n := extractBytesNSize(inner); n > 0 {
			return fmt.Sprintf("scval.Bytes%dSliceToScVal(%s)", n, expr)
		}
		// otherwise assume it's a slice of structs
		return fmt.Sprintf("scval.StructSliceToScVal(%s)", expr)
	}

	// Struct - call ToScVal
	return fmt.Sprintf("scval.MustToScVal((%s).ToScVal())", expr)
}

func snakeToPascal(s string) string {
	parts := strings.Split(s, "_")
	for i, part := range parts {
		if len(part) > 0 {
			parts[i] = strings.ToUpper(part[:1]) + part[1:]
		}
	}
	return strings.Join(parts, "")
}

func camelToSpaces(s string) string {
	var result strings.Builder
	for i, r := range s {
		if i > 0 && unicode.IsUpper(r) {
			result.WriteRune(' ')
		}
		result.WriteRune(r)
	}
	return strings.ToLower(result.String())
}
